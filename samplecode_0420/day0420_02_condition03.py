f_number = float(input("학점을 입력해주세요> "))

if f_number == 4.5:
    print("신")
elif f_number >= 4.2:
    print("교수님의 사랑")
elif f_number >= 3.5:
    print("현 체제의 수호자")
elif f_number >= 2.8:
    print("일반인")
elif f_number >= 2.3:
    print("일탈을 꿈꾸는 소시민")
elif f_number >= 1.75:
    print("오락문화의 선구자")
elif f_number >= 1.0:
    print("불가촉천민")
elif f_number >= 0.5:
    print("자벌레")
elif f_number > 0.0:
    print("플랑크톤")
else:
    print("시대를 앞서가는 혁명의 씨앗")